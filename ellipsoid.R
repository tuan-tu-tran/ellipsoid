ellipsoid<-function(
	hx=10,
	hrot=15,
	h=100,
	a=1,
	b=1,
	c=1,
	theta=45,
	phi=15,
	zlim=-1,
	scale=F,
	...
)
{
	if(zlim==-1)
	{
		zlim=c(-c,c);
	}
	pmat=persp(
		c(-a,a),
		c(-b,b),
		matrix(nrow=2,ncol=2),
		xlab="x",
		ylab="y",
		zlab="z",
		theta=theta,
		phi=phi,
		zlim=zlim,
		scale=scale,
		...
	);
	x=a*cos(seq(0,pi,pi/hrot));
	z=c*cos(seq(0,pi,pi/hrot));
	rot=seq(0,2*pi,pi/hrot);
	lenz=length(z);
	lenx=length(x);
	lenrot=length(rot);
	for(i in 1:lenx)
	{
		X=x[i];
		Ry=abs(b*sqrt(a^2-X^2)/a);
		Rz=abs(c*sqrt(a^2-X^2)/a);
		ptz=c();
		ptx=c();
		pty=c();
		for(j in 1:lenrot)
		{
			alpha=rot[j];
			Y=Ry*cos(alpha);
			Z=Rz*sin(alpha);

			ptz<-c(ptz,Z);
			ptx<-c(ptx,X);
			pty<-c(pty,Y);
		}
		res=trans3d(ptx,pty,ptz,pmat);
		points(
			res$x,
			res$y,
			type="l"
		);
	}
	for(i in 1:lenz)
	{
		Z=z[i];
		Ry=abs(b*sqrt(c^2-Z^2)/c);
		Rx=abs(a*sqrt(c^2-Z^2)/c);
		ptz=c();
		ptx=c();
		pty=c();
		for(j in 1:lenrot)
		{
			alpha=rot[j];
			Y=Ry*sin(alpha);
			X=Rx*cos(alpha);

			ptz<-c(ptz,Z);
			ptx<-c(ptx,X);
			pty<-c(pty,Y);
		}
		res=trans3d(ptx,pty,ptz,pmat);
		points(
			res$x,
			res$y,
			type="l"
		);
	}
}

res=ellipsoid();
